#include "libft.h"

int		ft_count_words(char const *str, char c)
{
	int	count;

	count = 0;
	while (*str != '\0')
	{
		while (*str && *str == c)
			str++;
		if (*str && *str != c)
		{
			count++;
			while (*str && *str != c)
				str++;
		}
	}
	return (count);
}
